"""
        The idea of building Mind Garden did not arrive in a single moment. It formed slowly, through repeated layers of helplessness.

        In real life and in films, I often see scenes where a person with Alzheimer's disease gradually stops recognizing the people closest to them. They forget the partner who stays beside them each day. They forget the family they spent most of their life with. Sometimes the unfamiliarity turns into fear or rejection. Whenever I see this, I feel a quiet ache, and a deeper fear: what if this happens to someone I love? If ten or twenty years of being together becomes only a blurred shadow in their memory, what would that feel like?

        It also reminds me of a line from the film *Coco*: **"The real death is that no one in the world remembers you."** The story suggests that what keeps a person present is not the body, but being remembered, having their story spoken. Memory of love, and being remembered, is one of the warmest and strongest links between people. If someone is remembered, they do not fully disappear. If even that last memory is erased, that is a different kind of loss. In the same way, watching someone slowly forget the people closest to them can feel more frightening than separation itself. That is why I keep wishing to do something, earlier, before "being forgotten" arrives.

        This repeated worry led me to a question: can we notice cognitive change earlier? Can caring about a parent's cognitive health feel less distant and less hard?

        In truth, many families have almost no chance to observe changes in a parent’s cognition. Not because they do not care, but because they do not know what to look for, lack the background, have no suitable tools, are busy, or assume forgetfulness is simply normal aging.

        So important signals get missed, and care quietly turns into "we will deal with it when something happens." By the time decline is obvious, years may have passed.

        That is where Mind Garden took root.

        I do not want it to be a medical product that adds pressure. I also do not want it to make users feel assessed or diagnosed. Instead, I hope it can be a gentle, acceptable, sustainable entry point, where family members can slowly build a basic understanding of cognition through natural interaction.

        Through light activities, Mind Garden helps users experience core ideas related to Attention, Short-term Memory, and Cognitive Flexibility. It does not offer medical conclusions. It offers a reason to observe, and an opening to speak. If it can lead to one conversation, one exchange, or a little earlier attention, then it has already done what it came to do.

        Often what we lack is not expertise, but a starting point that lets us act. Mind Garden wants to be that starting point.

